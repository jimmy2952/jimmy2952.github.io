let section_hover = document.getElementById('section-image');

let CITY_TP = document.getElementById('CITY_TP');
let taipeicity = document.getElementById('taipeicity-description');

CITY_TP.onmouseover = function() {
    section_hover.classList.add('taipeicity', 'animated', 'slideInUp');
    taipeicity.classList.add('animated', 'slideInUp')
    taipeicity.style.opacity = 1;  
}

CITY_TP.onmouseleave = function() {
    section_hover.classList.remove('taipeicity', 'animated', 'slideInUp');
    taipeicity.style.opacity = 0;
    taipeicity.classList.remove('animated', 'slideInUp')
}

